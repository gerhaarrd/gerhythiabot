def translate(key: str, lang: str = "en", **kwargs: Any) -> str:
    """Translate a key to the target language, falling back to English if key is missing."""
    lang = lang.lower() if lang else "en"
    if lang not in LANGUAGES:
        lang = "en"

    # Get translated pattern, fallback to English, fallback to key itself
    pattern = TRANSLATIONS.get(lang, {}).get(key)
    if pattern is None:
        pattern = TRANSLATIONS.get("en", {}).get(key, key)

    try:
        return pattern.format(**kwargs)
    except Exception:
        return pattern
